<#
.SYNOPSIS
    Train XGBoost model
.DESCRIPTION
    PowerShell equivalent of train.sh
#>

param(
    [string]$Config,
    [string]$TrainingDataPath,
    [string]$RunName,
    [string]$PythonScript,
    [switch]$Help
)

# =====================
# Usage
# =====================
function Show-Usage {
    Write-Host "Usage: .\train.ps1 [options]"
    Write-Host ""
    Write-Host "Options:"
    Write-Host "  -Config PATH              Path to config YAML"
    Write-Host "  -TrainingDataPath PATH    Path to training data"
    Write-Host "  -RunName NAME             Run name (default: autogenerated)"
    Write-Host "  -PythonScript PATH        Path to training script"
    Write-Host "  -Help                     Show this help message"
    exit 1
}

if ($Help) { Show-Usage }

# =====================
# Resolve paths
# =====================
$SCRIPT_DIR = $PSScriptRoot
$PROJECT_ROOT = (Resolve-Path (Join-Path $SCRIPT_DIR "..\..")).Path

Write-Host "SCRIPT_DIR: $SCRIPT_DIR"
Write-Host "PROJECT_ROOT: $PROJECT_ROOT"

# =====================
# Defaults
# =====================
if (-not $PythonScript) {
    $PythonScript = Join-Path $PROJECT_ROOT "src\scripts\train.py"
}
if (-not $Config) {
    $Config = Join-Path $PROJECT_ROOT "src\config\logistic_regression.yaml"
}
if (-not $TrainingDataPath) {
    $TrainingDataPath = Join-Path $PROJECT_ROOT "src\data\train.csv"
}

$TIMESTAMP = Get-Date -Format "yyyyMMdd_HHmmss"
if (-not $RunName) {
    $RunName = "baseline-logistic_regression_$TIMESTAMP"
}

# =====================
# Python path
# =====================
$env:PYTHONPATH = $PROJECT_ROOT

# =====================
# Echo resolved config
# =====================
Write-Host "PYTHON_SCRIPT: $PythonScript"
Write-Host "CONFIG_PATH: $Config"
Write-Host "TRAINING_DATA_PATH: $TrainingDataPath"
Write-Host "RUN_NAME: $RunName"

# =====================
# Train
# =====================
python $PythonScript `
    --config $Config `
    --training-data-path $TrainingDataPath `
    --run-name $RunName

if ($LASTEXITCODE -ne 0) { exit $LASTEXITCODE }
